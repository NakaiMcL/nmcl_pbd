#calculator functions

#Addition 
add <- function(x, y) {
  return (x + y)
}
add(2,2)

#Substraction
subtract <- function(x, y){
  return (x - y)
}
subtract(5, 3)

#Multiplication
multiply <- function(x,y){
  return (x * y)
}
multiply(3, 4)

#Division
divide <- function(x, y){
  return (x / y)
}
divide(10, 2)

#Exponential
exponential <- function(x, y){
  return (x ^ y)
}
exponential(2, 6)

#Ten to the power x
ten_power <- function(x){
  return (10 ** x)
}
ten_power(3)

#cube
cube <- function(x){
  return (x ^ 3)
}
cube(4)

#square
squared <- function(x){
  return (x ^ 2)
}
squared(8)

#Square root
square <- function(x){
  return( x ** 0.5)
}
square(25)

#Cosine
cosine <- function(x){
  return(cos(x))
}
cosine(4)

#Sine
Sine <- function(x){
  return(sin(x))
}
Sine(4)